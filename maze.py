    def draw(self, screen):
        BLUE = (0, 0, 255)
        for y, row in enumerate(self.maze):
            for x, cell in enumerate(row):
                if cell == 1:  # 壁を描画する
                    pygame.draw.rect(screen, BLUE, (x * self.block_size, y * self.block_size, self.block_size, self.block_size))

    def is_wall(self, grid_x, grid_y):
        """ 指定したグリッド座標が壁かどうかを判定する """
        if 0 <= grid_y < len(self.maze) and 0 <= grid_x < len(self.maze[0]):
            return self.maze[grid_y][grid_x] == 1
        return True  # 迷路外はすべて壁扱い